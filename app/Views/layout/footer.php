    const el = document.getElementById("floatingVideo");

    el.addEventListener('mousedown', dragMouseDown);

    function dragMouseDown(e) {
        if (e.target.tagName === 'VIDEO' || e.target.classList.contains('close-btn')) return; // ป้องกันลากจาก video
        e.preventDefault();

        let pos3 = e.clientX;
        let pos4 = e.clientY;

        document.onmouseup = closeDragElement;
        document.onmousemove = elementDrag;

        function elementDrag(e2) {
            e2.preventDefault();
            const pos1 = pos3 - e2.clientX;
            const pos2 = pos4 - e2.clientY;
            pos3 = e2.clientX;
            pos4 = e2.clientY;

            el.style.top = (el.offsetTop - pos2) + "px";
            el.style.left = (el.offsetLeft - pos1) + "px";
        }

        function closeDragElement() {
            document.onmouseup = null;
            document.onmousemove = null;
        }
    }
</script>

